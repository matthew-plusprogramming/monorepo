/**
 * test-writer-unlock-marker.mjs — cryptographic marker mint/verify utility.
 *
 * Shared library module imported by both:
 *   - .claude/scripts/session-checkpoint.mjs (mint side, record-test-writer-unlock)
 *   - .claude/hooks/<test-writer-isolation-hook>.mjs (verify side, PreToolUse)
 *
 * Purpose
 * -------
 * Hook-level verification of the test-writer-unlock claim must be cryptographic,
 * not a string match. String fields in session.json are forgeable by any agent
 * with write access to the session document; an HMAC keyed on a per-session
 * secret that only session-checkpoint.mjs can read makes forgery computationally
 * infeasible. The secret file itself is protected via FULL_BLOCK and
 * filesystem mode 0600 (see as-004b; spec §Open Questions Q1 resolution).
 *
 * Contract (from as-004 Acceptance Criteria)
 * ------------------------------------------
 *   AC4.1: mintMarker is deterministic — same inputs → same token.
 *   AC4.2: verifyMarker returns true on token + matching inputs.
 *   AC4.3: verifyMarker returns false on any single mismatched input
 *          (specGroupId, dispatchId, unlockedUntil, secret).
 *   AC4.4: Verification uses crypto.timingSafeEqual (constant-time compare).
 *   AC4.5: Token length and format are stable and documented here (see below).
 *
 * Token format (AC4.5)
 * --------------------
 * Output of mintMarker is a base64url-encoded string (per Task 4.2 of as-004
 * and spec.md Task 4). Underlying digest is HMAC-SHA256 (256 bits = 32 bytes);
 * base64url encoding of 32 bytes yields a 43-character string with no
 * padding (`=` suffix stripped per RFC 4648 §5 base64url convention used by
 * Node's `digest('base64url')`). Length is stable across all inputs.
 *
 *   token.length === 43
 *   token characters ∈ [A-Za-z0-9_-]
 *
 * Audit-log consumers can rely on these invariants when parsing entries.
 *
 * Message canonicalization
 * ------------------------
 * The HMAC input message is a deterministic concatenation of the four binding
 * fields, separated by a single NUL byte (0x00):
 *
 *   message = specGroupId || "\x00" || dispatchId || "\x00" || unlockedUntil
 *
 * The NUL separator prevents boundary confusion where differing field splits
 * could collide (e.g., "ab" + "c" vs "a" + "bc" both concatenating to "abc").
 * NUL is disallowed in the spec-group-id / dispatch-id / ISO-8601 timestamp
 * field values — the field validators in session-checkpoint.mjs enforce this.
 *
 * The HMAC key is the per-session secret bytes (32 random bytes generated by
 * session-checkpoint.mjs start-work; see as-004b). The secret is passed as a
 * Buffer or string; mintMarker/verifyMarker do not read the secret from disk.
 *
 * Non-goals
 * ---------
 * This module does NOT:
 *   - Read or write any filesystem state (pure function module).
 *   - Rotate secrets (session-checkpoint.mjs owns lifecycle).
 *   - Include first-failure-ref in the signed message (per as-004 Task 4.2
 *     signature; spec.md Q1 "64-hex-char" format note is superseded by the
 *     shipped implementer contract).
 *
 * @req REQ-005 (AC-005.4, AC-005.5)
 * @spec sg-pipeline-efficiency-ws2-practice-2.4 / spec.md
 */

import { createHmac, timingSafeEqual } from 'node:crypto';
import { Buffer } from 'node:buffer';

/**
 * Mockable seam for test suites asserting the AC4.4 call path.
 *
 * Rationale: Node's ESM module namespaces are frozen — `vi.spyOn(crypto,
 * 'timingSafeEqual')` throws `Cannot redefine property`. Routing the compare
 * through a mutable exported object gives tests a stable seam without changing
 * production call shape:
 *
 *   import * as markerModule from './test-writer-unlock-marker.mjs';
 *   vi.spyOn(markerModule.__cryptoSeam, 'timingSafeEqual');
 *
 * The seam is documented, named with a double-underscore prefix per convention
 * for "internal but exported for testability", and is not intended for
 * production rebinding. Security invariant: swapping the function would break
 * constant-time compare — this is acceptable risk in the trust model (tests
 * only; production callers never reassign the seam).
 *
 * @internal Exported solely to permit spying in unit tests (AC4.4).
 */
export const __cryptoSeam = {
  timingSafeEqual,
};

/**
 * Field separator — NUL byte, disallowed in all bound field values.
 * See "Message canonicalization" in the module header.
 */
const FIELD_SEPARATOR = '\x00';

/**
 * HMAC algorithm identifier. SHA-256 per spec.md §Open Questions Q1.
 */
const HMAC_ALGORITHM = 'sha256';

/**
 * Encoding used to serialize the digest output. base64url per as-004 Task 4.2.
 * Node's built-in 'base64url' encoding strips padding and uses URL-safe chars.
 */
const TOKEN_ENCODING = 'base64url';

/**
 * Expected token byte length for integer-length comparison before
 * timingSafeEqual. HMAC-SHA256 output is always 32 bytes.
 */
const DIGEST_BYTE_LENGTH = 32;

/**
 * Internal — build the canonical HMAC message from the bound fields.
 *
 * @param {{specGroupId: string, dispatchId: string, unlockedUntil: string}} fields
 * @returns {string}
 */
function canonicalMessage({ specGroupId, dispatchId, unlockedUntil }) {
  validateField('specGroupId', specGroupId);
  validateField('dispatchId', dispatchId);
  validateField('unlockedUntil', unlockedUntil);
  return [specGroupId, dispatchId, unlockedUntil].join(FIELD_SEPARATOR);
}

/**
 * Internal — reject empty, non-string, or separator-containing field values.
 *
 * @param {string} name
 * @param {unknown} value
 */
function validateField(name, value) {
  if (typeof value !== 'string' || value.length === 0) {
    throw new TypeError(`test-writer-unlock-marker: ${name} must be a non-empty string`);
  }
  if (value.includes(FIELD_SEPARATOR)) {
    throw new TypeError(
      `test-writer-unlock-marker: ${name} may not contain the NUL byte separator`,
    );
  }
}

/**
 * Internal — normalize the secret to a Buffer (accepts Buffer or string).
 *
 * @param {Buffer|string} secret
 * @returns {Buffer}
 */
function normalizeSecret(secret) {
  if (Buffer.isBuffer(secret)) {
    if (secret.length === 0) {
      throw new TypeError('test-writer-unlock-marker: secret buffer must be non-empty');
    }
    return secret;
  }
  if (typeof secret === 'string' && secret.length > 0) {
    return Buffer.from(secret, 'utf8');
  }
  throw new TypeError(
    'test-writer-unlock-marker: secret must be a non-empty Buffer or string',
  );
}

/**
 * Mint an HMAC-SHA256 cryptographic marker token for a test-writer unlock
 * claim.
 *
 * Deterministic: identical inputs always yield the identical token string
 * (AC4.1). The HMAC key is caller-supplied — session-checkpoint.mjs passes the
 * per-session secret loaded from `.claude/coordination/.session-hmac-<session-id>`.
 *
 * @param {object} input
 * @param {string} input.specGroupId   Spec group identifier (e.g., sg-...).
 * @param {string} input.dispatchId    Dispatch identifier recorded at unlock time.
 * @param {string} input.unlockedUntil ISO-8601 TTL expiry timestamp.
 * @param {Buffer|string} input.secret Per-session HMAC key (non-empty).
 * @returns {string} base64url-encoded token, 43 characters.
 */
export function mintMarker({ specGroupId, dispatchId, unlockedUntil, secret }) {
  const message = canonicalMessage({ specGroupId, dispatchId, unlockedUntil });
  const key = normalizeSecret(secret);
  return createHmac(HMAC_ALGORITHM, key).update(message, 'utf8').digest(TOKEN_ENCODING);
}

/**
 * Verify a cryptographic marker token against a claimed {specGroupId,
 * dispatchId, unlockedUntil} tuple and the per-session secret.
 *
 * Uses `crypto.timingSafeEqual` for constant-time digest comparison to avoid
 * byte-level timing oracles that would leak which prefix of the token matched
 * (AC4.4). Length mismatches, decode failures, and field-shape violations
 * return `false` — they do not throw. Only invariant violations that cannot
 * occur with a well-formed caller (e.g., missing / empty required fields on
 * the claimed tuple) throw a TypeError.
 *
 * @param {object} input
 * @param {string} input.token          Previously minted token (base64url).
 * @param {string} input.specGroupId    Claimed spec group.
 * @param {string} input.dispatchId     Claimed dispatch.
 * @param {string} input.unlockedUntil  Claimed ISO-8601 TTL expiry.
 * @param {Buffer|string} input.secret  Per-session HMAC key.
 * @returns {boolean} true if the recomputed digest matches the token.
 */
export function verifyMarker({ token, specGroupId, dispatchId, unlockedUntil, secret }) {
  if (typeof token !== 'string' || token.length === 0) {
    return false;
  }
  // Field-shape violations on the claim side are caller bugs — surface them.
  // Token decode / length mismatches are adversarial inputs — return false.
  const expected = mintMarker({ specGroupId, dispatchId, unlockedUntil, secret });

  const expectedBuf = Buffer.from(expected, TOKEN_ENCODING);
  let candidateBuf;
  try {
    candidateBuf = Buffer.from(token, TOKEN_ENCODING);
  } catch {
    return false;
  }
  // Node's Buffer.from(..., 'base64url') does not throw on invalid chars; it
  // silently drops them. The defensive path is a length check before the
  // constant-time compare — mismatched length → reject.
  if (candidateBuf.length !== DIGEST_BYTE_LENGTH || expectedBuf.length !== DIGEST_BYTE_LENGTH) {
    return false;
  }
  return __cryptoSeam.timingSafeEqual(candidateBuf, expectedBuf);
}

/**
 * Exported constants for test and audit-log consumer introspection.
 * Not part of the primary API; useful for schema validation.
 */
export const MARKER_TOKEN_LENGTH = 43; // base64url(32 bytes) without padding
export const MARKER_ALGORITHM = 'HMAC-SHA256';
export const MARKER_ENCODING = TOKEN_ENCODING;
